@app.route('/api/savings', methods=['POST'])
def calculate_savings():
    """Расчёт трат на курение"""
    data = request.json
    cigs = data.get('cigarettes', 0)
    price = data.get('price', 0)
    
    if cigs <= 0 or price <= 0:
        return jsonify({'error': 'Введите корректные значения'})
    
    cost_per_cig = price / 20
    daily_cost = cigs * cost_per_cig
    monthly_cost = daily_cost * 30
    yearly_cost = daily_cost * 365
    
    return jsonify({
        'daily': round(daily_cost),
        'monthly': round(monthly_cost),
        'yearly': round(yearly_cost)
    })

